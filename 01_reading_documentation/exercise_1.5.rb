#Using the ruby documentation, determine how you can write large numbers in a way that makes them easier to read.

=begin 
From: https://docs.ruby-lang.org/en/3.2/syntax/literals_rdoc.html#label-Integer+Literals

You can write integers of any size as follows:

1234
1_234
These numbers have the same value, 1,234. The underscore may be used to enhance readability for humans. You may place an underscore anywhere in the num
=end
